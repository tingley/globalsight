## GBS-3780 jobs have comments from different companies

CREATE TABLE JOB_COMMENTS
(
  ID BIGINT
     PRIMARY KEY,
  CREATE_DATE DATETIME
     NOT NULL,
  CREATOR_USER_ID VARCHAR(80)
     NOT NULL,
  COMMENT_TEXT VARCHAR(4000),
  COMMENT_OBJECT_ID BIGINT
     NOT NULL
) ;

INSERT INTO JOB_COMMENTS SELECT ID,CREATE_DATE,CREATOR_USER_ID,COMMENT_TEXT,COMMENT_OBJECT_ID FROM COMMENTS WHERE COMMENT_OBJECT_TYPE = 'J';

